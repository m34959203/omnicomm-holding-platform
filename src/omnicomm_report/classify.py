"""Классификатор «транспорт vs стационарный/сервисный объект».

В фиде КАП телематика стоит не только на машинах: АЗС, топливные ёмкости,
ФЭС (солнечные станции), дизель-генераторы, насосные — они приходят как
«объекты» и засоряют денежные рейтинги ТС («солнечная станция жжёт 37 тыс ₸»)
и подрывают доверие к цифрам. Здесь — консервативный фильтр по имени: исключаем
только явно нетранспортные объекты, не трогая спорное.

Источник правды по таксономии — [04 knowledge-base], это эвристика до её ввода.
"""

from __future__ import annotations

import re

# Явные маркеры НЕтранспортных объектов (стационар/инфраструктура/сервис).
_NON_TRANSPORT = (
    "азс", "ёмкост", "емкост", "резервуар", "цистерн", "топливозаправочн",
    "фэс", "дэс", "солнечн", "генератор", "jetpower", "aksa", "bs power",
    "bscu", "котельн", "насосн", "складск", "трансформатор", "подстанц",
    "квт", " кв·", "kw ", "дизельн станц", "электростанц",
)
# Имя-IMEI/серийник: только цифры (и разделители), длинное — безымянный объект.
_IMEI = re.compile(r"^[\d\s\-]{10,}$")


def is_transport(name: str | None) -> bool:
    """True — это транспортное средство; False — стационарный/сервисный объект."""
    if not name:
        return True  # без имени не отсекаем (fail-open, не теряем реальные ТС)
    n = name.strip().lower()
    if _IMEI.match(name.strip()):
        return False
    return not any(m in n for m in _NON_TRANSPORT)


def split_transport(names):
    """(транспорт, нетранспорт) — список имён по классу (для диагностики)."""
    t, nt = [], []
    for nm in names:
        (t if is_transport(nm) else nt).append(nm)
    return t, nt
