"""Генератор демонстрационной Excel-выгрузки автопарка (режим Б).

Создаёт samples/fleet_sample.xlsx со структурой, близкой к реальной выгрузке
Omnicomm: русские заголовки, запятая как десятичный разделитель в части полей,
один «шумовой» столбец «Возможные сливы топлива» — он НЕ должен попасть в отчёт
(проверка инварианта ТЗ §9).

Запуск: python samples/generate_sample.py
"""

from __future__ import annotations

import os

import pandas as pd

ROWS = [
    # госномер, пробег, расход, расход/100, моточасы, без движения ч, топливо без движ., макс скорость, превышения, пробег с прев., слив(шум)
    ("А123ВС 01", 6420, 4810, None, 312, 88, 1180, 96, 12, 210, 0),
    ("В456ОР 02", 5180, 3920, None, 268, 64, 760, 142, 31, 540, 0),     # аномальная макс скорость
    ("С789КХ 03", 0, 180, None, 41, 39, 180, 0, 0, 0, 0),               # нулевой пробег при расходе
    ("Е012МН 05", 7310, 6240, None, 401, 132, 2010, 88, 5, 90, 0),      # высокий простой двигателя
    ("К345ТУ 07", 4990, 3110, None, 214, 41, 520, 79, 2, 30, 0),
    ("Н678РА 09", 8120, 9980, None, 455, 96, 1740, 104, 18, 360, 0),    # высокий расход на 100
    ("Т901ОВ 11", 3870, 2440, None, 176, 28, 360, 82, 1, 15, 0),
    ("У234СН 13", 5640, 4020, None, 251, 55, 690, 91, 9, 140, 0),
    ("Х567ЕМ 15", None, None, None, None, None, None, None, None, None, 0),  # нет данных
    ("М890КР 17", 6750, 5230, None, 338, 71, 980, 118, 22, 410, 0),
]

COLUMNS = [
    "Госномер",
    "Пробег, км",
    "Расход топлива, л",
    "Расход на 100 км",
    "Моточасы",
    "Работа двигателя без движения, ч",
    "Топливо без движения, л",
    "Максимальная скорость",
    "Превышения скорости",
    "Пробег с превышением, км",
    "Возможные сливы топлива",  # шумовой столбец — игнорируется загрузчиком
]


def main() -> str:
    df = pd.DataFrame(ROWS, columns=COLUMNS)
    out = os.path.join(os.path.dirname(__file__), "fleet_sample.xlsx")
    df.to_excel(out, index=False)
    print(f"Создан {out} — {len(df)} ТС")
    return out


if __name__ == "__main__":
    main()
